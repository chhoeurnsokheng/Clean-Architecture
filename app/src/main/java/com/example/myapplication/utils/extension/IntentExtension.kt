        e.printStackTrace()
        false
    }
}

fun Context.openFacebookPage(url: String): Boolean {
    return try {
        packageManager.getPackageInfo("com.facebook.katana", 0)
        val intent = Intent(Intent.ACTION_VIEW, Uri.parse(url))
        startActivity(intent)
        true
    } catch (e: Exception) {
        e.printStackTrace()
        false
    }
}

fun Context.openFacebookPageById(pageId: String): Boolean {
    return try {
        packageManager.getPackageInfo("com.facebook.katana", 0)
        Intent(Intent.ACTION_VIEW, Uri.parse("fb://page/$pageId"))
        true
    } catch (e: Exception) {
        e.printStackTrace()
        false
    }
}

fun Context.openGoogleMapsDirection(latitude: Double, longitude: Double): Boolean {
    return try {
        val uri = Uri.parse("google.navigation:q=$latitude,$longitude")
        val mapIntent = Intent(Intent.ACTION_VIEW, uri)
        mapIntent.setPackage("com.google.android.apps.maps")
        startActivity(mapIntent)
        true
    } catch (e: Exception) {
        e.printStackTrace()
        false
    }
}
